class Solution {
    public boolean isBinaryPalindrome(int n) {
        String s = Integer.toBinaryString(n);
        int left = 0;
        int right = s.length() - 1;
        
        while (left < right) {
            if (s.charAt(left) != s.charAt(right)) {
                return false;
            }
            left++;
            right--;
        }
        
        return true;
    }
}